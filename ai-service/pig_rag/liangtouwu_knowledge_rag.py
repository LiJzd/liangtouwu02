def init_liangtouwu_knowledge_db(force_rebuild: bool = False) -> Chroma:
    """
    初始化或重建金华两头乌专属知识库本地 ChromaDB。
    """
    if not DASHSCOPE_API_KEY:
         raise ValueError("初始化两头乌知识库失败：未检测到 DASHSCOPE_API_KEY 配置。")

    embeddings = DashScopeEmbeddings(
        model=EMBEDDING_MODEL,
        dashscope_api_key=DASHSCOPE_API_KEY,
    )

    db_path = Path(CHROMA_PERSIST_DIR)
    
    # 检查数据库是否已存在，且未请求强制重建
    if db_path.exists() and not force_rebuild:
        print(f"[RAG-Liangtouwu] 检测到已存在的 Chroma 向量数据库目录: {CHROMA_PERSIST_DIR}，正在加载...")
        try:
            return Chroma(persist_directory=CHROMA_PERSIST_DIR, embedding_function=embeddings)
        except Exception as e:
            print(f"[RAG-Liangtouwu] 加载已存在库失败，尝试强制重建。错误原因: {e}")
            force_rebuild = True

    # 强制重建逻辑：物理清理已有目录并重新导入
    if db_path.exists() and force_rebuild:
        print(f"[RAG-Liangtouwu] 正在清理已存在的本地 ChromaDB 数据库: {CHROMA_PERSIST_DIR} ...")
        shutil.rmtree(CHROMA_PERSIST_DIR, ignore_errors=True)

    print(f"[RAG-Liangtouwu] 正在物理构建本地 ChromaDB 数据库: {CHROMA_PERSIST_DIR} ...")
    
    # 构建元数据：主要是知识类型标记，方便未来可能进行的分类过滤
    metadatas = []
    for index, text in enumerate(LIANGTOUWU_KNOWLEDGE_CHUNKS):
        # 简单提取前 10 个字符作为分类标记
        title_start = text.find("【")
        title_end = text.find("】")
        category = "通用"
        if title_start != -1 and title_end != -1:
            category = text[title_start+1 : title_end]
        
        metadatas.append({
            "chunk_id": index,
            "category": category,
            "breed": "两头乌"
        })

    vectorstore = Chroma.from_texts(
        texts=LIANGTOUWU_KNOWLEDGE_CHUNKS,
        embedding=embeddings,
        metadatas=metadatas,
        persist_directory=CHROMA_PERSIST_DIR,
    )
    
    print(f"[RAG-Liangtouwu] Chroma 向量库构建成功，已安全导入 {len(LIANGTOUWU_KNOWLEDGE_CHUNKS)} 个核心知识切片。")
    return vectorstore


