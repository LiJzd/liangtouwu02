_vectorstore: Optional[Chroma] = None

def _get_vectorstore() -> Chroma:
    """懒加载获取全局 Chroma 实例"""
    global _vectorstore
    if _vectorstore is None:
        _vectorstore = init_liangtouwu_knowledge_db(force_rebuild=False)
    return _vectorstore


def query_liangtouwu_knowledge(query_text: str, top_n: int = 3) -> List[Dict[str, Any]]:
    """
    语义检索金华两头乌专属知识库。
    
    返回：
    List[Dict[str, Any]]: 包含相似度匹配的文本切片内容及相似度得分（Chroma 返回的是 L2 距离得分，越小越相似）。
    """
    db = _get_vectorstore()
    
    # similarity_search_with_score 返回 List[Tuple[Document, float]]
    results = db.similarity_search_with_score(query=query_text, k=top_n)
    
    formatted_results = []
    for doc, score in results:
        formatted_results.append({
            "text": doc.page_content,
            "score": round(float(score), 4),
            "metadata": doc.metadata
        })
        
    return formatted_results


